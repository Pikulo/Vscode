#转义字符
print('hello\nworld')
print('hellooo\tworld')
print('hellooooo\tworld')
print('hello\rworld\ryou are fool\rkiddy')
print('hello\bworld')
print('http:\\\\www.baidu.com')

print('老师说：“大家好”')
#原字符 不希望字符串中的转义字符起作用，就使用原字符，就是在字符串之前加上r,或R
print(r'老师说：\n“大家好”')
#注意 字符串最后不能是\
print(r'老师说：\n“大家好”\\\\\\')
print(2*2*2*2*2*2*2*2)


































































































