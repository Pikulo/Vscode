s=float(input("输入分数"))
if(90<=s<=100):
    print("你的成绩等级为A")
elif(80<=s<90):
    print("你的成绩等级为B")
elif(70<=s<80):
    print("你的成绩等级为C")
elif(60<=s<70):
    print("你的成绩等级为D")
elif(s<60):
    print("你的成绩等级为E")
else:
    print("对不起，您的成绩不在有效范围内")
























